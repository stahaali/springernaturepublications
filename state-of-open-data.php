        <div class="row mb-4">
            <h3 class="subheading border-white">The State of Open Data 2023 white paper </h3>
        </div>
        <div class="row">
            <div class="col-lg-10">
                <p class="textinfo">The 2023 survey showed that the key motivations for researchers to share their data remain similar to previous years, with full citation of research papers or a data citation ranking highly. 89% of respondents also said they make their data available publicly.</p>

                <h4 class="textgray2 mb-4">One size does not fit all</h4>
                <p clas="textinfo">Variations in responses from different areas of expertise and geographies highlight a need for a more nuanced approach to research data management support globally. For example, 64% of respondents supported the idea of a national mandate for making research data openly available.</p>

                <h4 class="textgray2 mb-4">Credit is an ongoing issue</h4>
                <p>For eight years running, our survey has revealed a recurring concern among researchers: the perception that they don't receive sufficient recognition for openly sharing their data. 60% of respondents said they receive too little credit for sharing their data.</p>
            </div>
            <div class="col-lg-2">
                <a href="https://doi.org/10.6084/m9.figshare.24428194">
                    <img src="assets/images/blog/226.webp" class="img-fluid" alt="">
                </a>
            </div>

            <h4 class="textgray2 mb-4">AI awareness hasn’t translated to action</h4>
            <p>We asked survey respondents to indicate if they were using ChatGPT or similar AI tools for data collection, data processing and metadata collection. The most common response to all three questions was ‘I’m aware of these tools but haven’t considered it.’</p>
