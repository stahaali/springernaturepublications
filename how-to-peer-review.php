<section class="gray-section">
    <div class="container">
        <div class="row mb-4">
            <h3 class="subheading border-white">How the peer review process works</h3>
        </div>
        <div class="row mb-4">
            <p class="textinfo">Journals use peer review to both validate the research reported in submitted manuscripts, and sometimes to help inform their decisions about whether or not to publish that article in their journal.</p>
            <p>If the Editor does not immediately reject the manuscript (a “desk rejection”), then the editor will send the manuscript to two or more experts in the field to review it. The experts—called peer reviewers—will then prepare a report that assesses the manuscript, and return it to the editor. After reading the peer reviewer's report, the editor will decide to do one of three things: reject the manuscript, accept the manuscript, or ask the authors to revise and resubmit the manuscript after responding to the peer reviewers’ feedback. If the authors resubmit the manuscript, editors will sometimes ask the same peer reviewers to look over the manuscript again to see if their concerns have been addressed. This is called re-review.</p>
            <p>Some of the problems that peer reviewers may find in a manuscript include errors in the study’s methods or analysis that raise questions about the findings, or sections that need clearer explanations so that the manuscript is easily understood. From a journal editor’s point of view, comments on the importance and novelty of a manuscript, and if it will interest the journal’s audience, are particularly useful in helping them to decide which manuscripts to publish.</p>
        </div>
        <div class="row mt-4">
            <h4 class="textblue3 mb-4">Will the authors know I am a reviewer? Will I know who the authors are?</h4>
            <p>Traditionally, peer review worked in a way we now call “closed,” where the editor and the reviewers knew who the authors were, but the authors did not know who the reviewers were. In recent years, however, many journals have begun to develop other approaches to peer review. These include:</p>
            
            <div class="list-1 mt-4">
                <ul>

                    <li><div><i class="fa-solid fa-check"></i></div> <div>Closed peer review—where the reviewers are aware of the authors’ identities but the authors’ are never informed of the reviewers’ identities.</div></li>
                    <li><div><i class="fa-solid fa-check"></i></div> <div>Double-blind peer review—where neither author nor reviewer is aware of each other’s identities.</div></li>
                    <li><div><i class="fa-solid fa-check"></i></div> <div>Open peer review—where authors and reviewers are aware of each other’s identity. In some journals with open peer review the reviewers’ reports are published alongside the article.</div></li>

                </ul>
            </div>

            <p>The type of peer review used by a journal should be clearly stated in the invitation to review letter you receive and policy pages on the journal website. If, after checking the journal website, you are unsure of the type of peer review used or would like clarification on the journal’s policy you should contact the journal’s editors.</p>

        </div>
    </div>
</section>

<section class="white-section">
    <div class="container">
        <div class="row mb-4">
            <h3 class="subheading">Why serve as a peer reviewer?</h3>
        </div>
        <div class="row">
            <p class="textinfo mb-4">As your career advances, you are likely to be asked to serve as a peer reviewer.</p>
            <p>As well as supporting the advancement of science, and providing guidance on how the author can improve their paper, there are also some benefits of peer reviewing to you as a researcher:</p>
            
            <div class="list-1 list-3">
                <ul>
                    <li> Serving as a peer reviewer looks good on your CV as it shows that your expertise is recognized by other scientists. (See the supplemental material about the Web of Science Reviewer Recognition Service to learn more about getting credit for the reviews you do. Also see the supplemental material about ORCiD iDs to learn how to connect your reviews to your unique ORCiD iD.) </li>
                    <li> You will get to read some of the latest science in your field well before it is in the public domain.</li>
                    <li> The critical thinking skills needed during peer review will help you in your own research and writing.</li>
                </ul>
            </div>
        </div>

        <div class="row mt-4">
            <h3 class="textblue3 mb-4">Who does peer review benefit?</h3>
            <p>When performed correctly peer review helps improve the clarity, robustness and reproducibility of research.</p>
            <p>When peer reviewing, it is helpful to think from the point of view of three different groups of people:</p>
        </div>

        <div class="row mt-4">
            <div class="list-1 list-3">
                <ul>
                        <li><i class="fa-solid fa-check"></i> Authors. Try to review the manuscript as you would like others to review your work. When you point out problems in a manuscript, do so in a way that will help the authors to improve the manuscript. Even if you recommend to the editor that the manuscript be rejected, your suggested revisions could help the authors prepare the manuscript for submission to a different journal. </li>
                        <li><i class="fa-solid fa-check"></i> Journal editors. Comment on the importance and novelty of the study. Editors will use your comments to assess whether the manuscript is of the right level of impact for the journal. Your comments and opinions on the paper are much more important that a simple recommendation; editors need to know why you think a paper should be published or rejected as your reasoning will help inform their decision.</li>
                        <li><i class="fa-solid fa-check"></i> Readers. Identify areas that need clarification to make sure other readers can easily understand the manuscript. As a reviewer, you can also save readers’ time and frustration by helping to keep unimportant or error filled research out of the published literature.</li>
                </ul>
            </div>

            <p>Writing a thorough, thoughtful review usually takes several hours or more. But by taking the time to be a good reviewer, you will be providing a service to the scientific community.</p>
        </div>
    </div>
</section>

<section class="gray-section">
    <div class="container">
        <div class="row">
            <h3 class="subheading border-white mb-4">Accepting an invitation to review</h3>
            <p class="textinfo">Editors invite you to review as they believe that you are an expert in a certain area. They would have judged this from your previous publication record or posters and/or sessions you have contributed to at conferences. You may find that the number of invitations to review increases as you progress in your career.</p>
            <p>There are several questions to consider before you accept an invitation to review a paper.</p>
        </div>
        <div class="row mt-4">
            <div class="list-1 list-4">
                <ul>
                    <li><div class="number">1</div>  <div>Are you qualified? The editor has asked you to review the manuscript because he or she believes you are familiar with the specific topic or research method used in the paper. It will usually be okay if you can review some, but not all, aspects of a manuscript. Take as an example, if the study focused on a certain physiological process in an animal model you conduct your research on but used a technique that you have never used. In this case, simply review the parts of the manuscript that are in your area of expertise, and tell the editor which parts you cannot review. However, if the manuscript is too far outside your area, you should decline to review it.</div></li>
                    <li><div class="number">2</div>  <div>Do you have time? If you know you will not be able to review the manuscript by the deadline, then you should not accept the invitation. Sending in a review long after the deadline will delay the publication process and frustrate the editor and authors. Keep in mind that reviewing manuscripts, like research and teaching, is a valuable contribution to science, and is worth making time for whenever possible.</div></li>
                    <li><div class="number">3</div>  <div>Are there any potential conflicts of interest? You should evaluate the manuscript as fairly and objectively as possible. Potential conflicts of interest include:</div></li>
                </ul>
            </div>

            <div class="list-1 list-4 px-5 mt-4">
                <ul>
                    <li><div class="number">1</div> <div>The reported results could cause you to make or lose money, e.g., the authors are developing a drug that could compete with a drug you are working on.</div></li>
                    <li><div class="number">2</div> <div>The manuscript concerns a controversial question that you have strong feelings about (either agreeing or disagreeing with the authors).</div></li>
                    <li><div class="number">3</div> <div>You have strong positive or negative feelings about one of the authors, e.g., a former teacher who you admire greatly.</div></li>
                    <li><div class="number">4</div> <div>You have published papers or collaborated with one of the co-authors in recent years.</div></li>
                </ul>
            </div>
        </div>
        <div class="row mt-4">
            <p>If you are not sure if you have a conflict of interest, discuss your circumstances with the editor.</p>
            <p>Along with avoiding a conflict of interest, there are several other ethical guidelines to keep in mind as you review the manuscript. Manuscripts under review are highly confidential, so you should not discuss the manuscript – or even mention its existence – to others. One exception is if you would like to consult with a colleague about your review; in this case, you will need to ask the editor’s permission. It is normally okay to ask one of your students or postdocs to help with the review. However, you should let the editor know that you are being helped, and tell your assistant about the need for confidentiality. In some cases case, when the journal operates an open peer review policy they will allow the student or postdoc to co-sign the report with you should they wish.</p>
            <p>It is very unethical to use information in the manuscript to make business decisions, such as buying or selling stock. Also, you should never plagiarize the content or ideas in the manuscript.</p>
            <p>Next: <a href="#" class="txt-gray">Evaluating manuscripts</a></p>
        </div>
    </div>
</section>

