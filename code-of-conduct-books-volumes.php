                                        Conflict of interest
                                    </button>
                                </h2>
                                <div id="collapseSix" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                        <div class="list-1 list-3">
                                            <ul>
                                                <li><i class="fa-solid fa-check"></i>Editors are requested to disclose interests that are directly or indirectly related to the work submitted for publication.</li>
                                                <li><i class="fa-solid fa-check"></i>Disclosure of interests provides a more complete and transparent process and helps readers form their own judgements of potential bias. This is not meant to imply that a financial relationship with an organisation that sponsored the research or compensation received for consultancy work is inappropriate. Interests may include but are not limited to the following: funding (grants, other forms of research support such as salaries, equipment, supplies, reimbursement for attending symposia, and other expenses), employment, financial interests (stocks, shares, consultation fees, patents and patent applications) and non-financial interests (professional interests, personal relationships or personal beliefs such as a position on an editorial board, advisory board or board of directors or other type of management relationships; writing and / or consulting for educational purposes; expert witness; mentoring relations).</li>
                                            </ul>
                                        </div>
                                    </div>
                                </div>
