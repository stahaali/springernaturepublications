                    <div class="row">
                        <p class="mb-4">For the third blog in the series, we caught up with the Director of Technical Services and Assessment at the University of Miami Libraries, which is part of the Springer Nature NERL transformative agreement (TA) in the United States.</p>
                        <h4 class="textgray mb-4">Tell us a little bit about yourself and your role in the University of Miami Libraries.</h4>
                        <p>I live in Miami with my spouse, three kids, and our dog. I serve as the Director of Technical Services and Assessment at the University of Miami Libraries. I hold a master’s in information science and am a Microsoft Certified Data Analyst Associate.</p>
                        <p>My role as Director of Technical Services and Assessment involves providing guidance and strategic management to library staff responsible for acquiring, describing, and ensuring access to materials in different formats, languages, and disciplines. I supervise the acquisition, assessment, licensing, and management of electronic resources, cataloguing, metadata creation, and processing of all library materials.</p>
                        <p>Additionally, I am involved in negotiating new agreements that support sustainable scholarship principles, streamlining workflows, particularly relating to OA agreements, and ensuring access and discovery of both owned and leased content.</p>
                        <p>I enjoy working together with my Technical Services team and guiding them to achieve success.</p>
                    </div>
                    <div class="row mt-4">
                        <div class="border-left p-3">
                            <p class="m-0"><i>“The decision to join this agreement was backed by data analysis on University of Miami publications and usage.”</i></p>
                        </div>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">What are your team’s role and responsibilities in managing OA agreements?</h4>
                        <p>Managing OA agreements is a task alongside other projects and responsibilities. Typically, I handle OA requests in a way similar to how I manage daily emails.</p>
                        <p>We also have a task force of about ten librarians that meets weekly to discuss current subscriptions and licensing agreements, explore trends and opportunities, and review our OA agreements and their performance.</p>
                        <p>We are now in the first year of the Springer Nature OA agreement, which has benefited our researchers by supporting their need to publish OA. The decision to join this agreement was backed by data analysis on University of Miami publications and usage.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Do you have any tips to offer to other libraries that are taking up responsibilities of OA publishing as part of an OA agreement?</h4>
                        <p>I have several concrete suggestions. First establish strict and clear criteria for approving or denying OA publishing requests. Additionally, make sure you have access to systems that can verify the requester's affiliation with your institution. I think it is important to ensure that your team takes ownership of this new task, which involves reviewing, approving, and denying OA requests, similar to handling emails.</p>
                        <p>Lastly, closely track statistics and data, as they are essential for assessing if an OA agreement truly meets your institution's needs.</p>
