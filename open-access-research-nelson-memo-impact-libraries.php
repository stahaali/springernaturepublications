                    <div class="row mt-4">
                        <p>Beginning with Plan S in 2018 the pace of transformation to open access publishing has picked up in recent years and legislation is now a driving force of change. The EU Directive is at implementation stage, while in the U.S. the OSTP Nelson memo requires federal agencies to produce compliant public access policies by December 31st 2024 which will come into effect December 31st 2025.</p>
                        <p>It has taken a long time for open access titles to be regarded as having the same prestige as subscription journals for promotion and tenure – which has historically disincentivised researchers from publishing in them. The Nelson memo comprehensively levels this playing field. The 250,000 – 270,000 articles a year that acknowledge federal funding will be now be published with some form of open access. However, researchers like to publish where their peers are, so this move may well have an impact on the prevalence of open access publication of privately funded research as well.</p>
                        <p>This pace of change is good for open science but can feel exhausting for those who have to do the work.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">The library and the publication process</h4>
                        <p>In the traditional publishing model, the library was at the end of the publication process; the ultimate destination for research that could then be accessed by library patrons. With open access publishing the route to publication goes through the library, and this is a substantial change to workflows and processes.</p>
                        <p>Librarians help researchers understand where they can publish, and (with an increase in predatory journals) evaluate journals to make sure they are a safe and appropriate place to publish research.</p>
                        <p>If using transformative agreements to achieve OSTP compliance, then librarians and research administrators will be involved between acceptance of an article and publication. They’ll be ensuring that an article falls under an agreement that allows the researcher to publish at no cost or helping to navigate APC payments.</p>
                        <p>In broad terms, recent legislative changes ask researchers to take responsibility for equitable access to research through their publishing choices, and librarians and research administrators are the natural people to help facilitate this change. They are often the department with the most detailed knowledge of publishers. They hold the existing relationships and are practiced at negotiating deals.</p>
                        <p>The change to open access doesn’t fundamentally change what a library does – organise access to academic research – but it does change how this is done. Libraries need new structures and workflows that make research accessible to readers.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Empowering librarians to overcome cost and resource challenges</h4>
                        <p>So while librarians agree in principle with open access, and are among its most vocal champions and advocates, a recent Association of Research Libraries survey identified costs and resourcing as main concerns about the implementation of open access for many more articles. How can librarians address these concerns and what changes need to make so that libraries can play a full role in facilitating open access?</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Understand the data about research and funders</h4>
                        <p>One useful first step is to use data to provide a more concrete picture of how the move to OA might affect your institution. This introduction to a useful tool and study will help librarians get an overall picture of the data and dive into it for a more detailed look. One issue the study uncovered was the need for disambiguation of institutions, agencies and relationships. Funding bodies have both umbrellas and subsidiaries and there will have to be rapid development of metadata to help institutions assess which research need to be published in an OSTP compliant way.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Evaluate the skills you have and the gaps you need to fill</h4>
                        <p>It hasn’t been that many years since a major aspect of every library role was the shelving and organising of books and journals. As online resources became more important, and the access and licensing around them become more complicated, the role of Electronic Resources Management Librarian developed to meet this need.</p>
                        <p>Now this role is arguably mutating into the Scholarly Communications Librarian –and NASIG usefully describes the core competencies of library role that manages intellectual output and publisher relationships.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Acknowledge the complexity of your organisation</h4>
                        <p>Researchers are not a consistent block. Their workflows are very different, some are funded, some write alongside teaching. Others concentrate on fieldwork while some may have complex outputs that involve the need to archive software as well as results.</p>
                        <p>Librarians need to learn broad approaches from their peers but implement workflow solutions that respond to the needs of their own institution. Some highly focussed institutions may only need deals with a few large publishers to cover federally funded research. Others may be in a more complex situation.</p>
                        <p>Researchers can be supported by different funders with slightly different requirements and while publishing OA research will need to be compliant with local legislation, there may be many minor exceptions to workflow. There may also be different combinations of article publication and open data provision.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Create and evaluate an author validation workflow</h4>                        
                        <p>Identified by early adopters as a new workflow that needs to be established, the process of verifying that authors are eligible to publish under a transformative agreement often falls to the library as the owner of the relationship with publishers.</p>
                        <p>With time lags between submission and publication, researchers moving on to take up new posts, and research groups stretching across institutions, establishing which researchers and papers are eligible to publish in which journals is a task that is usually simple, but occasionally more difficult.</p>
                        <p>Clear written policies, that are easy to find and comprehend, go a long way to helping researchers understand when they are eligible under agreements, and when they are not.</p>
                    </div>
                    
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Make the case for costs</h4>
                        <p>Due to their extensive experience with publishers, librarians are best placed to help researchers understand their obligations and navigate the publishing process. However it is worth noting that the changes in workflow and process do not change the mission of the library  or the essential role of the librarian. Indeed, as the focus of the library shifts from providing information about research in other places, to disseminating the work of the institution it serves, arguably the library becomes more central to the prestige, impact and reputation of its home institution.</p>
                        <p>This shift in focus may in its initial phases need to cover the cost of establishing new processes and workflows, but in the long run these will enhance the reputation of the institution by making its research more influential but making it easy to find and reference.</p>
                    </div>
                    
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Libraries can meet the pace of change</h4>
                        <p>For open access advocates, including many librarians, the increased pace of change driven by legislation is an exciting opportunity that will transform the way scientific and academic knowledge is disseminated. Behind the scenes, librarians and publishers are working hard to create the new processes and workflows that will become the architecture underpinning academic discourse. In practical terms, this is likely to mean establishing processes that meet the needs of most of the research, and understanding where exceptions and variations to workflow are needed. In this new landscape, librarians will continue to be the bridge between an institution and the wider landscape of academic knowledge.</p>
                        <p>If you’re a librarian, take the first step today: connect with your colleagues, explore new publishing options, and advocate for open access in your institution. For valuable insights and tools tailored for librarians, visit the Open Access Hub.</p>
