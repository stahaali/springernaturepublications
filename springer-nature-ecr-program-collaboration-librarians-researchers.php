                    <div class="row mt-4">
                        <p>The Early Career Research (ECR) Program innovatively brings together many buzzwords but in a meaningful and effective way for all those who take part. “As issues of equity, open access (OA), and research integrity become more important in the age of AI, it is paramount that our librarian and researcher communities know that we are a resource and understand how to get the information and support they need from us,” says the program’s initiator, Eseohe Arhebamen-Yamasaki.</p>

                        <h4 class="textgray mb-4 mt-4">Advancing DEI and supporting early career researchers</h4>

                        <p>Eseohe is no stranger to these “buzzwords”: At Springer Nature, she is a member of the Research diversity, equity, and inclusion (DEI) Steering Group, its United States and African Research Advisory Councils Taskforces, its Global Black Researcher Taskforce, and various Sustainable Development Goal working groups. </p>
                    </div>
                    
                    <div class="row mt-4">
                        <h4 class='textgray mb-4'>The ECR Program: Structure and benefits</h4>
                        <p>The ECR Program is a format for multilateral engagement between a university’s library, research office, and researchers and Springer Nature. The specific topics that the engagement will focus on are determined by the needs and interest of the university, specifically its researchers. The ECR Program is built on dialogue, and each of its sessions is as unique and exclusive as the university it takes place in and the interests of its researchers.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class='textgray mb-4'>Workshop engagement: Topics and facilitation</h4>
                        <p>The engagement takes the form of a workshop covering various topics in academic publishing services and resources offered by Springer Nature. The workshops are facilitated by Springer Nature communications experts, with additional support from account development and publishing/editorial teams, depending on the discussion topics. Discussion topics range from broadly discussing the academic publishing landscape to analysing the university’s publishing patterns; from manuscript writing and journal selection to artificial intelligence, new technologies for authors, understanding OA, and creating a submission strategy; from DEI in research and in publishing to the Sustainable Development Goals. One highly relevant issue for ECRs is turning their dissertation into a book, and in the workshop, they have a chance to get expert insights and tips.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class='textgray mb-4'>Benefits for university libraries and research offices</h4>
                        <p>For the university library, the ECR Program augments its efforts in information literacy and training and development programs, by providing invaluable information to their staff and their researcher community. Tailoring educational offerings to researchers’ interests will help researchers gain in-depth knowledge on key areas such as reproducibility, peer review, research integrity, DEI, OA publishing, predatory publishing, and more. They also help to build a better research community. “This is an opportunity for the library to ask their community what they want to know and work with us to bridge that knowledge gap,” Eseohe explains.</p>
                        <p>Research offices also stand to gain from the closer partnership with Springer Nature that this program facilitates, as well as from the opportunity to further connect with their library, students, and researchers. Sharing their own work and areas of focus at our ECR Program events supports and strengthens the relationship with their own library, aiding intra-university collaboration.</p>
                    </div>
                    <div class="row mt-4">
                        <h4>ECR Program’s pilot workshops</h4>
                        <p>The ECR Program already held four pilot events in US universities. When it comes to determining what an event should cover, Eseohe’s approach is very straightforward: “the community determines the topics of these workshops itself. This is a bespoke process, not a one-size fits all.” Eseohe’s counterparts at the university library share what their researcher community wants to know more about, and the topics and line up for the event are built to accommodate their needs and interests.</p>

                        <div class="list-1 list-3">
                            <ul>
                                <li>
                                    <div>
                                        <i class="fa-solid fa-check"></i>
                                    </div>
                                    <div>
                                        At Syracuse University (October 2023), two in-person workshops focused on understanding academic publishing, specifically peer review and open access. To be able to thoroughly respond to in depth questions from their researchers on the peer review process, as well as inquiries regarding university support for publishing OA, the workshops were tailored to address these topics and to equip the librarians with knowledge that enables them to support their researchers.
                                    </div>
                                </li>
                                <li>
                                    <div>
                                        <i class="fa-solid fa-check"></i>
                                    </div>
                                    <div>
                                        At Case Western Reserve University (April 2024), a hybrid (both in person and virtual) workshop on peer review and reproducibility aimed to give librarians the knowledge and tools needed to offer their researchers support on these issues. “It was particularly edifying,” Eseohe recounts, that the librarian was “very well versed in exactly what topics her researchers wanted more information on.” Offering a virtual option, when possible, aids inclusivity, as not all are able to attend events in person.
                                    </div>
                                </li>
                                <li>
                                    <div>
                                        <i class="fa-solid fa-check"></i>
                                    </div>
                                    <div>
                                        At Howard University (April 2024), a workshop on publishing and DEI addressed diversity, equity, and inclusion considerations and initiatives in publishing. This workshop was an opportunity to hear from the community about their specific needs, and the university counterparts also presented the role and focus of their research office. Howard University is one of the more famous of the Historically Black Colleges and Universities, and this presented a unique experience for Eseohe: “I most enjoyed the one-on-one conversations with researchers for whom the in-person presence of a publishing representative who looks like them signalled that they are truly welcome in the community.”
                                    </div>
                                </li>
                                <li>
                                    <div>
                                        <i class="fa-solid fa-check"></i>
                                    </div>
                                    <div>
                                        At the University of Pennsylvania (May 2024), a workshop was held on peer review and OA, during which a poll among ECRs at the university exposed that more than half were not sure what OA is. This event was held in a round room that fostered a relaxed conversation in which researchers felt comfortable to share their excitement about transformative agreements that enable OA publishing or frustrations with long peer review times.
                                    </div>
                                </li>

                            </ul>
                        </div>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">The unique value of ECR Program workshops</h4>
                        <p>While every community is unique and interactions are different, the experience and dialogue at the workshops in the ECR Program show the thirst for knowledge and in depth understanding of the publication process and a desire to engage to find ways to better support researchers. When faced with unawareness and lack of familiarity with something like OA, for instance, Eseohe finds encouragement for the Program and the value it adds: “Despite communicating via press releases, announcements, and blog posts with our communities, there is almost no replacement for in-person gatherings at which attendees are more likely to ask questions, especially if the atmosphere is less formal,” she says.</p>
                    </div>
                     <div class="row mt-4">
                        <div class="border-left">
                            <p>
                                “Meeting with our librarian and researcher communities in person provides a special opportunity for them to ask questions directly of us instead of having to search online for answers. Having met in person makes us more accessible, and when attendees tell us about initiatives they are planning, opportunities for collaboration arise which we would not otherwise have.” - Eseohe Arhebamen-Yamasaki, Head of Communications at Springer Nature
                            </p>
                            
                        </div>
                        <p class="mt-4">Interestingly, the ECR Program events create opportunities for collaboration also within the institution as the visibility of librarians increases and information between participants is openly shared. In one case, Eseohe shares, a student wrote about OA publishing for their university newspaper following their participation in an ECR Program event in consultation with their university librarian. “It feels wonderful to have been involved in connecting this community with their librarian while providing them with tools to better understand and engage with university policy and around OA,” Eseohe says contentedly.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">What’s next for the ECR Program?</h4>
                        <p>Springer Nature’s ECR Steering Committee and Task Force will discuss the next steps for this program and its expansion. Eseohe anticipates the development of multimodal presentation materials to meet the library and researcher need for targeted learning around key topics. Also planned is the optimisation of offerings to provide more career development opportunities for the research community, such as the ECR Program’s Rising Scholars initiative, to provide more career development opportunities for the research community.</p>
                        <p>The underlying goal is to collaborate with librarians and empower them to support and advance their researchers, especially ECRs. Eseohe’s work involves understanding the needs of librarians and working to develop engagement and support for the community. The ECR Program embodies this, as well as her passion for engagement and interactions with the communities Springer Nature serves.</p>
                        <p>What support do your ECRs need? Explore Springer Nature’s resources for ECRs as well as the recently published book The Early Career Researcher’s Toolbox.</p>
                    </div>
