                            <p>A poet, multimedia artist and music producer with over 20 years of experience in strategic communications and media relations, Eseohe Arhebamen-Yamasaki is U.S. Head of Communications for Springer Nature. She leads Springer Nature's Early Career Researcher (ECR) Program which coalesces the company's efforts globally to support ECRs' success in the research and publishing ecosystem as part of Springer Nature's vision of an open science and open access future.</p>
