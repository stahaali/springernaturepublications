                        </div>
                    </div>
                </div>
                <div class="row mt-4">
                    <p>In this blog, the second in a series on OA in China, we analysed the importance of institutions’ role in influencing authors to choose OA or not, and what institutional support would benefit Chinese researchers the most in the expansion of OA.</p>
                    
                    <h4 class="textgray2 mt-4 mb-4">Institutional influence on authors’ OA choice</h4>
                    
                    <p>The 2024 China OA Attitudes survey shows although many different individuals and organisations can influence an author’s OA decision, the strongest external influence on authors’ OA decisions is their institution. When authors were asked to select which of a group of organisations and people had any influence on a recent decision to publish gold OA, 64% selected their institution. This does not even include additional votes on research office (38%) or library (22%) in their institution. As the top 1 source of external influence, institutions can exert an influence in various forms, including institutional blacklists and whitelists, Article Processing Charge (APC) thresholds, reimbursement policies etc. These institutional influences form the basis of authors’ submission decisions.</p>

                    <div class="col-lg-10">
                        <img src="assets/images/blog/101.webp" class="img-fluid img-100" alt=""/>
                        <p><i>Figure 1: Q. ‘Which of the following organisations / people had any influence on your most recent decision to publish Gold open access?’ (n=202)</i></p>
                    </div>
                </div>
                <div class="row mt-4">
                    <p>Where barriers do still exist to OA, institutions are often best placed to help overcome them. When respondents were asked the reasons why they didn’t publish their most recent article as gold OA, issues around funding and paying APC charges were the biggest barrier for publishing via gold OA, selected by 59% of respondents. The lack of a gold OA mandate by the funder or institution was the second biggest barrier, reported by 36%.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray2 mb-4">Gaps between institutional support and author needs</h4>
                    <p>Overall, institutional support for publishing has increased compared to the survey of 2021. The most frequent form of support is blacklists of risky and unsuitable journals (57%). This is a new option added to the survey following the findings of previous research, and it well reflects blacklists like CAS Early Warning List have gained high attention among Chinese authors in recent years. An increase was found in other types of institutional support for OA: defined APC limits (rising by 15%), whitelists to advise journals for publishing, and support in organizing APC payments. Authors continue to receive less institutional support in terms of guidance on OA publishing and help on choosing the right publication license.</p>
                        <img src="assets/images/blog/102.webp" class="img-fluid img-100 mt-4" alt=""/>
                </div>
                <div class="row mt-4">
                    <p><i>Figure 2: Q. ‘What kinds of advice and support for publishing have you received from your institution, if any?’</i></p>
                    <p>Although authors have received a good amount of advice and support from their institutions, there is still a gap between existing support and what authors need most, especially in some areas. Above three quarters of the respondents reported they needed guidance on OA publishing, help on choosing the right license and journals for publication, as well as their institutions’ support for APC payment. The highest percentage (83%) comes from the authors who called for more institutional assistance on organising APC payment</p>
                        <img src="assets/images/blog/103.webp" class="img-fluid img-100 mt-3" alt=""/>
                        <p><i>Figure 3: Q. ‘Would you like to have more advice and support from your institution about publishing?’ (n=799)</i></p>
                        <p>In the open text responses to ‘Other’, respondents suggested areas where they would like more institutional support throughout the publishing process: extra funding for APCs, having a separate category in research grants for APCs, how to reimburse APCs with research grants, A forum for discussing the quality of journals etc.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray2 mb-4">Meeting the needs of researchers</h4>
                    <p>The survey clearly shows the predominance of institutions’ role in influencing authors’ OA choice, and there are still many areas where researchers want more support from their institutions, which is particularly the case around organizing and funding APCs. Funding and APC are always the most commonly mentioned barriers or support areas needed when Chinese authors consider publishing OA. To help researchers address those pain points and meet their real needs, institutions have a leading role to play. One possible solution institutions may adopt is to establish agreements with publishers to facilitate the payment and publishing process.</p>                    
                    <p>Springer Nature has been leading the transition to OA and offers different types of agreements that cover the publishing fees for authors from participating institutions. One type of OA agreements is a transformative agreement (TA) which combines reading and OA publication fees in hybrid journals into one single license.</p>
                    <p>We believe TAs offer clear advantages to everyone involved in research. For authors, publishing OA becomes a viable option because they don’t have to worry about payment. The agreements are discipline neutral, which is especially useful to researchers in areas that typically attract less funding, such as HSS. TAs increase the visibility, reach and impact of an institution’s research. They streamline workflows for institutions by centralising payments, while an easy OA workflow with automated, efficient and reliable author identification reduces administration.</p>
