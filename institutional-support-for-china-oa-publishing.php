                        <h3 class="subheading border-none">To publish OA, what institutional support do Chinese researchers need most?</h3>
                    </div>
                 <div class="row">
                    <div class="col-lg-8 col-md-8 col-12">
                        <p class="textinfo">China is the world’s biggest producer of research articles. How quickly the country moves to a full OA environment is a vital step change in terms of moving forwards the wider global transition to open science. Therefore, Springer Nature conducted a continuous survey on China OA perceptions, to explore researchers’ evolving attitudes, needs for institutional support and drivers and barriers behind their OA publishing.</p>
                    </div>
                    <div class="col-lg-4 col-md-4 col-12">
                        <div class="row align-items-center border-1 py-3">
                            <div class="text-box">
                                <div>
                                    <img src="assets/images/blog/100.webp" class="img-fluid" alt=""/>
