                                        Enfortumab Vedotin (Padcev): An antibody-drug conjugate for bladder cancer, providing an alternative for patients unresponsive to standard treatments.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">9</div>
                                    <div>
                                        Talquetamab (Talvey): A bispecific antibody for multiple myeloma, enhancing treatment options.
