                    <div class="row mt-4">
                        <h4 class="textgray">Advancements in treatment for cancer, Alzheimer’s, and more</h4>
                        <p>One of the most exciting aspects of the webinar was the discussion on breakthrough therapies. These therapies represent significant advancements in treatment options for various diseases, offering new hope to patients and healthcare providers alike. Some of the standout breakthrough therapies highlighted during the webinar include:</p>
                        
                        <div class="list-1 list-4">
                            <ul>
                                <li>
                                    <div class="number">1</div>
                                    <div>
                                        Aflibercept (Eylea HD): Developed by Regeneron Pharmaceuticals for eye disorders like AMD and diabetic retinopathy, offering a high-dose formulation to reduce the need for frequent injections.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">2</div>
                                    <div>
                                        Budesonide (Tarpeyo): An enteric-coated formulation for IgA nephropathy, significantly reducing kidney function loss.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">3</div>
                                    <div>
                                        Altuviiio: A factor VIII replacement therapy for hemophilia A, combining multiple proteins to extend circulation and reduce bleeding episodes.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">4</div>
                                    <div>
                                        Crispr/Cas9 Gene Therapy (Exa-cel): Developed by CRISPR Therapeutics and Vertex Pharmaceuticals for blood disorders like sickle cell disease and thalassemia, using CRISPR gene editing technology.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">5</div>
                                    <div>
                                        Mirikizumab (Omvoh): A monoclonal antibody for inflammatory bowel disease, targeting the IL-23 subunit to reduce bowel inflammation.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">6</div>
                                    <div>
                                        Abiraterone Acetate and Prednisone (Akeega): A combination therapy for prostate cancer, offering a potential treatment for BRCA-mutated mCRPC.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">7</div>
                                    <div>
                                        RSV Vaccine (Abrysvo): Developed by Pfizer, providing high levels of protection against RSV infections.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">8</div>
                                    <div>
                                        RSV Vaccine (Arexvy): Developed by GSK, competing with Pfizer’s Abrysvo in the RSV vaccine market.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">9</div>
                                    <div>
                                        Zolbetuximab (Viluet): A monoclonal antibody for gastric cancer, improving overall survival rates in patients with CLDN18.2-positive tumors.
                                    </div>
                                </li>
                            </ul>
