                                    <div class="number">10</div>
                                    <div>
                                        Ensifentrine (Enfexxa): An inhalation formulation for COPD, improving lung function and reducing inflammation.
                                    </div>
                                </li>
                                <li>
                                    <div class="number">11</div>
                                    <div>
                                        Imetelstat (Imetelstat): A telomerase inhibitor for myelodysplastic syndromes, offering a new treatment approach.
