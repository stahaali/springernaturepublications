                        <p>It was found that, under the UK Jisc agreement, the annual publication of OA articles in eligible Springer Nature journals increased by a compound annual growth rate (CAGR) of 12% year on year between 2016 and 2021, from 3,088 to 5,360 articles. Notably, this significant growth rate doesn’t even include the dramatic increase following the introduction of the agreement in 2016 (read more about the impact of UK Jisc agreement in our white paper).</p>
                        <p>Meanwhile, countries such as Norway, Sweden and Finland have seen 90%, 94% and 95% respectively of Springer Nature content now being published gold OA since the introduction of their TAs.</p>

                        <h4 class="textgray2 mt-4">Which journals are covered by transformative agreements?</h4>
                        
                        <p>Each TA is unique and therefore different journals are covered by the different agreements. You can download a list of journals covered by each agreement on this page.</p>

                        <h4 class="textgray2 mt-4">How do I publish under a transformative agreement?</h4>
                        
                        <p>You will need to be the corresponding author of the article and affiliated with a participating institution. During the author identification process, you’ll need to show your institution affiliation (you’ll be asked to do this once your article has been accepted for publication). The institution selected during this process should be your primary affiliation, as this institution will be asked to verify your eligibility.</p>

                        <p>You’ll also need to explicitly state your primary affiliation in the published manuscript, which is the institution at which the bulk of the research for the article was conducted.</p>
                        
                        <p>Again, because each TA is different, the best thing to do is to check if you are eligible to publish under an agreement (and find out how to do it if you are) on our OA agreements page for authors</p>

                        <p>And remember, we’re working diligently to establish new agreements all the time, so that more researchers could benefit, so even if you’re not covered by one right now, it’s worth checking back regularly. In the meantime, if you need help finding funding for APCs, we offer a free OA funding support service to help authors discover and apply for OA funding.</p>

