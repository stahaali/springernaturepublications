                    <div class="row mt-4">
                       <p>Publishing your research open access (OA) can bring a range of advantages both to you and to readers, from increased downloads and citations to making the research more accessible to marginalised communities. But publishing OA comes with a number of challenges too, such as finding funding for the article processing charges (APCs) and identifying an OA journal that’s a good fit for your research.</p>
                        <p>As part of our ongoing drive to advance OA publishing, we pioneered the use of transformative agreements (TAs), which help to address the challenges faced by researchers and remove barriers to publishing OA. </p>
                        <p>As we mark the addition of Nature, Nature Research and Palgrave journals to our OA agreement with Jisc in the UK, we’ve put together a guide to these agreements to help you understand what they are and how they can help you more easily publish your research OA.</p>
                        
                        <h4 class="textgray2 mt-4">What are transformative agreements?</h4>
                        
                        <p class="mt-4">Transformative agreements enable participating institutions to combine journal subscription access along with OA publication costs. This means that if you’re an author at an institution covered by a TA you may be able to publish your article OA with fees covered, in Springer Nature journals that are included in the agreement.</p>
                        <p>Many of our TAs are country-wide agreements. There are details of all the countries covered on our OA agreements page. Not only do they help researchers with publication fees, but they also make the administrative process of publishing more straightforward.</p>

                        <h4 class="textgray2 mt-4">What are the benefits of publishing open access under a transformative agreement?</h4>
                        
                        <p>For authors, publishing OA brings many benefits, from increasing the reach of research to broadening collaboration opportunities and complying with funder mandates (there’s more on benefits of OA here).</p>

                        <p>“By making our work more freely available to other researchers, including those from low-income countries, as well as journalists and interested readers, we are making the research process more inclusive,” explained Oliver Hauser, an Associate Professor of Economics at the University of Exeter Business School in the UK, who spoke to us as part of Open Access Week last year. “It also enables reproducibility and encourages discussion with a wider, more diverse audience that can dive into, learn from, and constructively critique our research.”</p>

                        <p>Not only does publishing under a TA bring all the benefits of OA, it also helps to reduce administration time and covers some or all of the APCs for researchers. This helps to make OA more accessible, even in disciplines such as Humanities and Social Sciences (HSS), where funding for OA publishing can be difficult to come by. Recent data shows that over 90% of HSS OA content in Springer Nature’s hybrid journals is now published via a TA, having grown at a faster rate than OA HSS content not published via a TA.</p>

                        <h4 class="textgray2 mt-4">Are transformative agreements having an impact?</h4>

                        <p>The short answer is yes. In countries where we’ve launched TAs, OA publishing has increased significantly.</p>

