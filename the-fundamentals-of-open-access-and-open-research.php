<section class="white-section">
    <div class="container">
        <div clas="row">
            <h3 class="subheading border-none textblue3 m-0">What is open access and open research?</h3>
            <p>Open access (OA) refers to the free, immediate, online availability of research outputs such as journal articles or books, combined with the rights to use these outputs fully in the digital environment. OA content is open to all, with no access fees.</p>
            <p class="mb-4">Open research goes beyond the boundaries of publications to consider all research outputs – from data to code and even open peer review. Making all outputs of research as open and accessible as possible means research can have a greater impact, and help to solve some of the world’s greatest challenges.</p>
        </div>
        <div clas="row">
            <h3 class="subheading border-none textblue3 m-0">How can I publish my work open access?</h3>
            <p>As the author of a research article or book, you have the ability to ensure that your research can be accessed and used by the widest possible audience. Springer Nature supports immediate Gold OA as the most open, least restrictive form of OA: authors can choose to publish their research article in a fully OA journal, a hybrid or transformative journal, or as an OA book or OA chapter.</p>
            <p class="mb-4">Alternatively, where articles, books or chapters are published via the subscription route, Springer Nature allows authors to archive the accepted version of their manuscript on their own personal website or their funder’s or institution’s repository, for public release after an embargo period (Green OA). Find out more.</p>
        </div>
        <div clas="row">
                <h3 class="subheading border-none textblue3 m-0">Why should I publish OA?</h3>            
        </div>
        <div class="row mt-4">
            <div class="col-lg-6 col-md-6 col-12">
                <p><strong>Increased citation and usage:</strong> Studies have shown that open access articles are viewed and cited more often than articles behind a paywall.</p>
                <p><strong>Greater public engagement:</strong> Content is available to those who can't access subscription content.</p>
                <p class="mb-4"><strong>Increased interdisciplinary conversation:</strong> Open access journals that cross multiple disciplines help researchers connect more easily and provide greater visibility of their research.</p>
            </div>
            <div class="col-lg-6 col-md-6 col-12">
                <p><strong>Wider collaboration:</strong> Open access publications and data enable researchers to carry out collaborative research on a global scale.</p>
                <p><strong>Faster impact:</strong> With Creative Commons licences, researchers are empowered to build on existing research quickly.</p>
                <p><strong>Compliance with open access mandates:</strong> Open access journals and books comply with major funding policies internationally.</p>
            </div>
        </div>
        
        <div class="row mt-4">
            <h3 class="subheading border-none textblue3">What are Creative Commons licences?</h3>
            <p>Open access works published by Springer Nature are published under Creative Commons licences. These provide an industry-standard framework to support re-use of OA material. Please see Springer Nature’s guide to licensing, copyright and author rights for journal articles and books and chapters for further information.</p>
        </div>

        <div class="row mt-4">
            <h3 class="subheading border-none textblue3">How do I pay for open access?</h3>
            <p>As costs are involved in every stage of the publication process, authors are asked to pay an open access fee  in order for their article to be published open access under a creative commons license. Springer Nature offers a free open access support service to make it easier for our authors to discover and apply for funding to cover article processing charges (APCs) and/or book processing charges (BPCs). Find out more.</p>
        </div>

        <div class="row mt-4">
            <h3 class="subheading border-none textblue3">What is open data?</h3>
            <p>We believe that all research data, including research files and code, should be as open as possible and want to make it easier for researchers to share the data that support their publications, making them accessible and reusable. Find out more about our research data services and policies.</p>
        </div>
        
        <div class="row mt-4">
            <h3 class="subheading border-none textblue3">What is a preprint?</h3>
            <p>A preprint is a version of a scientific manuscript posted on a public server prior to formal peer review. Once posted, the preprint becomes a permanent part of the scientific record, citable with its own unique DOI. Early sharing is recommended as it offers an opportunity to receive feedback on your work, claim priority for a discovery, and help research move faster. In Review is one of the most innovative preprint services available, offering real time updates on your manuscript’s progress through peer review. Discover In Review and its benefits.</p>
        </div>
        <div class="row mt-4">
            <h3 class="subheading border-none textblue3">What is open peer review?</h3>
            <p class="m-0">Open peer review refers to the process of making peer reviewer reports openly available. Many publishers and journals offer some form of open peer review, including BMC who were one of the first publishers to open up peer review in 1999. Find out more.</p>
        </div>
    </div>
</section>

