                    <h4 class="textgray mb-4">Avoiding citation manipulation</h4>
                    
                    <p>Citation manipulation misrepresents the importance of a particular piece of work and the journal in which it appears. It results in distortion of the scholarly record. If a poor-quality study is cited, this in turn can result in anything from wasted time (as other researchers try to replicate or build on results that are of less importance or soundness than implied) to implications for such things as healthcare decisions. Funding or employment opportunities may be misapplied as a consequence.</p>
                </div>
                
                <div class="row mt-4">
                    <div class="list-1 list-3">
                        <ul>
                              <li> <div><i class="fa-solid fa-check"></i></div> <div> Do ensure you clearly and accurately reference publications that are relevant to your work and manuscript.</div> <li>
                              <li> <div><i class="fa-solid fa-check"></i></div> <div> Don't include references only as favors to colleagues or in return for anything they offer you. </div><li>
                              <li> <div><i class="fa-solid fa-check"></i></div> <div> Do consider whether reviewer requests to include citations would enhance the paper or not (if the journal uses open peer review, where reviewers are identified to the authors, you may be able to tell when a reviewer is requesting references to their own work). If the reference would not enhance the paper or is not relevant to your work, you should point this out in your response comments and/or flag it to the Editor.</div> <li>
                              <li> <div><i class="fa-solid fa-check"></i></div> <div> Do consider requests from journals in a similar way, and politely decline them if the requested citations are not relevant. If a journal appears to be committing citation manipulation deliberately, e.g. rejecting manuscripts for refusing to include irrelevant citations, consider reporting the matter to the Publisher for investigation. Journals can face serious consequences for citation manipulation.</div><li>
