                    <h4 class="textgray mb-4">Study design and execution</h4>
                    <p>Carrying out the research with integrity includes clear and scrupulously honest documentation of what you do and the results obtained, complying with all appropriate guidelines and requirements for the type of research being carried out, and accurate, unbiased analysis of the results obtained. Poor planning or poor design can lead to a plethora of consequences (especially true if your work involves hazardous materials or procedures in any way). </p>
                    <p>Your results can be rendered meaningless, resources and budget can be wasted, your reputation and credibility damaged and any publications from the work impacted. Funding (present or future) can then be affected. By contrast, well researched and conducted work will be more likely to answer the question you are investigating and better able to stand up to scrutiny such as peer review. This can also have a positive effect on reproducibility and future reputation.</p>
                </div>
                
                <div class="row mt-4">
                    <h4 class="textgray mb-4">Data integrity and management</h4>
                    <p>Once research has begun, it is important to ensure that the resulting data, in whatever format it is produced, adheres to some basic tenets:</p>
                    
                    <div class="list-1 list-3 mt-4">
                        <ul>
                            <li><i class="fa-solid fa-check"></i> Data should never be fabricated, i.e. made up wholly or in part. This might seem obvious, but is still sadly prevalent, as seen in these high-profile examples from anesthesiology, evolutionary biology and biotechnology.</li> 
                            <li><i class="fa-solid fa-check"></i> Data should never be falsified. Analysis of data is obviously a key part of the research process, but this process should not change or omit data such that the research is no longer accurately represented. This would include removing individual data points or participants without appropriate justification, changing the brightness/contrast of a part of an image to enhance certain features etc.</li> 
                            <li><i class="fa-solid fa-check"></i> Data should be stored and archived, as well as backed up, to ensure integrity is maintained.</li>
                            <li><i class="fa-solid fa-check"></i> Maintain accurate metadata. Metadata refers to information about your data such as the project name, dates of collection, information about how and by whom data was collected and key parameters, units of measurement, etc. This is a crucial area with a lot of detail, and there is a detailed course, Managing Research Data, available from Nature Masterclasses to help.</li>
                        </ul>
                    </div>
                </div>
                
