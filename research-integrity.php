        </div>
        <div class="row mt-4">
            <p class="textinfo">Springer Nature and its journals are members of COPE (the Committee On Publication Ethics).  COPE’s primary mission is educational; it is an advisory body, not a regulator. There are more than 12,500 COPE members from 103 countries. Springer Nature’s Research Integrity Director, Chris Graf, was one of the first co-chairs of COPE. Our Research Integrity Group works closely with COPE to help them develop new educational resources</p>
            <p>COPE membership helps differentiate legitimate journals from the increasing number of predatory and poor-quality outlets. It is one of the indicators of quality used by important third parties such as Clarivate Analytics (for Journal Impact Factor) and indexers (e.g. Web of Science, Scopus, Google Scholar, PubMed). </p>
            
            <h4 class="textgray3 mb-4">Core Practices</h4>
            <p>COPE's Core Practices, set out clear guidance for publishers, including how allegations of misconduct should be handled; policies on authorship and contributorship, data availability, peer review process and ethics; and processes for complaints and appeals, managing conflict of interest/competing interest, journal management and post-publication discussion and corrections.</p>
            
            <h4 class="textgray3 mb-4">Guides</h4>
            <p>COPE publishes a number of guides for editors to help ensure the integrity of research publishing.</p>

            <h4 class="textgray3">Flowcharts</h4>
            <p class="mb-0">COPE flowcharts offer step by step practical support on handling different aspects of publication best practice. </p>
