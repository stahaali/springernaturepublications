<section class="white-section">
    <div class="container">
        <div class="row">
            <h3 class="subheading mb-4">Publication ethics</h3>
        </div>
        <div class="row">
            <div class="col-lg-3 col-md-3 col-12">
            <img src="assets/images/blog/165.webp" class="img-fluid" alt="">                
            </div>
            <div class="col-lg-9 col-md-9 col-12">
                
                <p>Mistakes by authors or issues relating to research and publication ethics can arise and be identified at any point along the manuscript handling process, as well as after publication. With increasing awareness and better detection methods, it is inevitable that an Editor-in-Chief will need to handle more and more publication ethics issues, so they should be aware of such issues, how to handle simple cases and where to go for help for more complicated issues.  </p>
                <p>All Springer Nature journals are members of the Committee on Publication Ethics (COPE) which provides advice and resources to help editors deal with research and publication problems. As well as a set of flowcharts to guide editors through commonly encountered problems, COPE also holds quarterly discussion forums for difficult cases and conferences for its members.</p>

                <div class="row mt-4">
                    <div class="accordion" id="accordionExample">
                        <div class="accordion-item">
                            <h2 class="accordion-header">
                            <button class="accordion-button" type="button" data-bs-toggle="collapse" data-bs-target="#collapseTwo" aria-expanded="true" aria-controls="collapseTwo">
                                When a publication ethics issue arises
                            </button>
                            </h2>
                            <div id="collapseTwo" class="accordion-collapse collapse show" data-bs-parent="#accordionExample">
                            <div class="accordion-body">
                                <p>
                                    With any publication ethics issue, there is likely to be a number of stakeholders involved, (e.g. authors, institutions, funders, reviewers, whistle-blowers). Editors-in-Chief can seek help and support from their Publisher contact in the first instance. In complex cases, a Research Integrity Advisor from the Springer Nature Research Integrity Group, a team dedicated to promoting best publication practice and resolving publication ethics issues, will get involved to help resolve the case.  
                                </p>
                                <p>
                                    It is important to remember that details of all on-going publication ethics investigations are kept confidential until a final editorial decision is made. The outcome of an investigation may be: no further action, the publication of a Correction, the publication of a Retraction, or the publication of an Expression of Concern.
                                </p>
                            </div>
                            </div>
                        </div>
                        <div class="accordion-item">
