                <div class="row mt-5">
                    <p>For researchers, sharing research outputs like publications, preprints, data, code, protocols, methods, or peer reviews—creates benefits for both the research community and society.</p>
                    <p>During OA week, Graham Smith, Open Data Programme Manager at Springer Nature, and Mark Hahnel, VP Open Research at Digital Science, discussed how enabling researchers to share the building blocks of research – data or code – can deliver a more equitable, transparent, and efficient research ecosystem. Yet despite these acknowledged benefits, sharing research data or code remains challenging for researchers.</p>
                    <p>Libraries are often a primary source of guidance for researchers navigating data-sharing requirements. Springer Nature’s new unified data and code policies aim to bridge the gap between policy and practice. By integrating open research practices into our journal and book workflows, authors can share their research outputs more easily, and other researchers can re-use their data and code to make new discoveries faster. The forthcoming State of Open Data report will provide practical insights into the role of policy in fostering data sharing and open research practices.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="text-gray2 mb-3">Equitable open access is a key part of the journey</h4>
                    <p>OA publishing is the foundation of our approach to open science. We have advocated for OA for over 20 years, offering authors trusted routes to publish their work openly. We have published more than 1.4 million primary research and review articles OA, including 183,000 OA articles in 2023.</p>
                    <p>Equity and accessibility are key to ensuring global participation and are therefore essential parts of our OA strategies. In an OA week webinar, Caroline Nevison, Director of Open Access Agreements and Sarah Phibbs, Director of Research4Life Publisher Partnerships, discussed strategies for advancing equity and accessibility in research. While challenges remain, there are several ways that barriers are increasingly being broken. These include APC waivers for researchers without access to funds, diamond OA journals, a dedicated Nature journals waiver fund, and our partnerships with libraries through our expanding transformative agreements (TA) programme.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray2 mb-3">Transformative agreements and equity </h4>
                    <p>TAs enable your authors to publish OA without bearing direct cost. Our TAs now support researchers at more than 3,700 institutions globally, driving seven times as many gold OA articles in Springer hybrid titles compared to authors choosing OA outside of a TA. This has led to a 46% year on year increase in OA articles, showing the demand for OA from researchers worldwide. </p>
                    <p>Crucially, as Caroline Nevison highlighted in the Spotlight On webinar, TAs have allowed us to expand access to previously underserved regions by offering flexible options, enabling TAs in Eastern Europe, South Africa, Egypt, India, Columbia, Mexico, and Thailand.</p>
                    <p>TAs also promote equity across disciplines by supporting the full academic community at participating institutions. By centralising funding, it becomes easier for researchers in less well-funded disciplines – particularly the Humanities and Social Sciences (HSS) – to publish OA. Our OA uptake continues to see the highest increase from HSS researchers, with a 36% uptake in 2023. Libraries tell us that this increased communication with patrons, across all disciplines, builds intra-departmental relationships. For example, Mark Robison, from the University of Notre Dame, found that after signing an OA agreement with Springer Nature it “greatly increased the campus’ goodwill towards the library.”  In advocating for OA to faculty, libraries are enhancing their visibility and relevance within their institutions.</p>
                    <p>Libraries supporting TAs further support the core missions of their organisations – offering access to the best research to everyone who needs it. Caroline Nevison also highlighted the important role TAs play in facilitating access to the Version of Record (VoR). Gold OA publication means that the VoR is immediately and permanently available, which is essential to truly deliver equity in research. Our data shows that articles published under TAs experience 10 times higher usage than non-OA articles, with particularly high download rates in Africa (10 times higher) and Asia (11 times higher). This demonstrates the broad impact of TAs in increasing access to research worldwide.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray2 mb-3">How OA benefits librarians</h4>
                    <p>While there are many cited benefits of open access for your researchers, including greater dissemination, readership and usage of their work, TAs offer additional benefits for participating libraries, from enriching the services you offer to your academic communities to streamlining workflows.</p>
                    <p>Librarians and approval managers are the institutional point of contact for researchers in their OA publication journey under an agreement. We have developed practical information and resources to support you with both administering and promoting OA agreements. These include ready-to-use assets such as web banners, email templates, and social media images, available on our Approval Manager resources page.</p>
                </div>
