                    <div class="row mt-4">
                        <p>Research integrity is a topic of relevance to anyone within the academic ecosystem. As librarians, it is essential to understand research integrity: What the issues are, how they are addressed, and by whom. This will enable you to best support your researchers and institutions, and to participate integrally in the efforts to uphold research integrity. In the webinar Research integrity and AI: Navigating challenges and leveraging potential, Chris offered a holistic overview of research integrity, which would benefit any academic librarian in their everyday work.</p>
                        <p>Chris leads Springer Nature’s Research Integrity Group, which implements tools, training, and processes that support research integrity, and ultimately help make research better. Chris has unparalleled expertise in research integrity and shares a wealth of information and insights in the webinar.</p>
                    </div>
                    
                    <div class="row mt-4">
                        <h4 class="textgray">Retracting published research to set the record straight</h4>
                        <p>“Integrity” is essentially about doing the right thing, and requires accountability, honesty, and courage. Chris opens his presentation by explaining how different actors in academic publishing can “do the right thing” in retracting published research.</p>
                        <p>For authors, this could mean working with publishers to retract their own publications when they discover errors or problems in their research, despite the potential risk to their reputation. Publishers also do the right thing when they retract publications that are found to be problematic.</p>
                        <p>While there are abundant examples of retractions, and Chris shares some interesting ones, it is important to note that retractions, even though they have increased in recent years for various reasons, are still less than 0.1% – a tiny fraction – of articles published in any one year.</p>
                    </div>
                    
                    <div class="row mt-4">
                        
                        <h4 class="text-gray">AI tools to detect integrity issues and avoid publication</h4>
                        <p>It is absolutely the right thing to retract a paper when any misconduct or mistakes are identified, but wouldn’t it be better if those articles weren’t published to begin with, Chris asks. This means investing in tools and procedures that identify and remove articles with integrity problems before they even reach the peer review stage in the publication process.</p>
                        <p>At Springer Nature, quality checks do just that. To give an idea of the scope, in October 2023, of the over 100,000 manuscripts submitted to Springer Nature, 11,000 (11%) were stopped before they could be sent to peer review, because they did not meet Springer Nature’s quality check requirements. Eventually, 2,300 of these manuscripts (2.3% of the total submitted) were removed from the process for a variety of integrity problems.</p>
                        <p>But how are these papers stopped and identified? Chris introduces two in-house tools developed by Springer Nature that use AI and other technology to support experts in making good and rapid decisions (“pretty cool stuff”, he says):</p>
                        <div class="list-1 list-3 mb-4 mt-4">
                            <ul>
                                <li>
                                    <div>
                                        <i class="fa-solid fa-check"></i>
                                    </div>
                                    <div>
                                        <strong>Geppetto</strong> is used to detect the inappropriate use of large language models (LLMs) in submissions. Chris stresses that it is legitimate for researchers to use new technology for their work and writing, including AI and LLMs, to help prepare legitimate work, providing its use is responsible, involves complete and careful human oversight and is fully documented. However these tools can also be misused to generate fake research, and Geppetto is intended to identify these kinds of manuscripts.
                                    </div>
                                </li>
                                <li>
                                    <div>
                                        <i class="fa-solid fa-check"></i>
                                    </div>
                                    <div>
                                        <strong>SnappShot</strong> helps to detect manipulated or duplicated images. Image integrity issues can be the result of human error or inappropriate use. Whether due to a mistake or misconduct, it is better to identify them and take appropriate action prior to publication.
                                    </div>
                                </li>
                            </ul>
                        </div>
                    </div>
                    
                    <div class="row">

                        <h4 class="textgray">Sharing knowledge to promote research integrity</h4>
                        
                        <p>Addressing integrity issues early on, whether by filtering out misconduct or helping authors address inadvertent mistakes, supports the broader research system, Chris explains. To promote research integrity, Springer Nature also provides training and resources for the benefit of researchers and other stakeholders:</p>
                        
                        <div class="list-1 list-3 mb-4">
                            <ul>
                                <li>
                                    <div>
                                        <i class="fa-solid fa-check"></i>
                                    </div>
                                    <div>
                                        Research integrity: An introduction for researchers. This online course, launched by Chris’s team, is free, self-paced, and 45 minutes long. It gives an overview of research ethics and publication ethics, essential to any researcher.
                                    </div>
                                </li>
                                <li>
                                    <div>
                                        <i class="fa-solid fa-check"></i>
                                    </div>
                                    <div>
                                        Research integrity: Publication ethics. This Nature Masterclasses course is also free and accessible online. It is more extensive, covering the essential steps for publishing work with the highest standards of integrity, and providing strategies to navigate writing and publishing with integrity.
                                    </div>
                                </li>
                            </ul>
                        </div>
                        
                        <p>Librarians are well positioned to advocate for research integrity and promote resources that educate researchers on the topic and keep them updated on innovation and best practices (as is the case for using AI in research). This is especially important considering that researchers often have no formal training on this important topic.</p>
                        <p>You can also download the list of AI eBooks highlights, with titles covering AI and emerging tech as well as AI and research integrity. This collated list is an excellent resource for librarians looking to support their academic communities in navigating the complexities of AI in research integrity.</p>
                    </div>
                    
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Enhancing research integrity through collaboration and human insight</h4>
                        <p>Two central themes persist through Chris’s presentation: The importance of collaboration to address integrity issues, and the human oversight of detection processes. Chris speaks excitedly about various collaborations across academic publishing that address the challenges of research integrity. These collaborations and partnerships, such as the Committee on Publication Ethics (COPE), involve publishers, academic institutions, and other stakeholders. They work collaboratively against paper mills and to combat the dangers of academic misconduct.</p>
                        <p><i>Watch the webinar recording to hear Chris discuss these issues in more detail and and download the AI eBook list for free.</i></p>
