                            <p class="textinfo">In this three-part blog series, we explore how to make the practice and publication of science more accessible to everyone. Each post offers a unique perspective on the necessary changes. A summary of the changes needed to follow OSTP guidelines explores new legal aspects of open access. Best practice on accessibility looks at how technical tools and protocols can drive access to published science. But we begin with the ethical perspective of Diversity, Equity and Inclusion (DEI); exploring how institutions can help students and researchers to create more inclusive environments for the study and practice of science.</p>
