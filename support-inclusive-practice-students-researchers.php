                    <div class="row mt-4">
                        <p>In both lectures and labs scientists have arguably made significant steps forward in understanding the broad issues that drive systemic discrimination and inequality, from entrenched attitudes that lead to unconscious bias against researchers from underrepresented backgrounds, to working practices that fail to make adjustments for researchers with a disability, the issues are widely acknowledged. Working to counteract them is, of course, more difficult than recognising them.</p>
                        <p>A recent Springer Nature survey of nearly 5000 researchers identified inertia around inclusivity practice as one barrier to a more inclusive work and study environment. The researchers surveyed felt that their institutions understood the issues but weren’t managing to address them. So to add to the infographic 5 ways institutions can support DEI we suggest 4 practical steps institutions can take to help students and researchers to be more inclusive.</p>
                    </div>
                    
                    <div class="row mt-4 ">
                        <h4 class="textgray mb-4">1. Are you listening?</h4>
                        <p>It’s essential for successful DEI strategies that the voices of different communities within an organisation are heard. At many institutions, student-run societies will represent the very groups who experience barriers to successful scientific careers. These include groups organised for people with disabilities, who are LGBTQIA+, or are members of particular religions. Peer groups can often have more honest and open conversations than those between student and lecturers, or management and staff. Building a sense of community between these groups, and staff and management at a university can do much to help inclusivity.</p>
                        <p>In the DEI survey underrepresented groups thought that many DEI initiatives were failing to meet their needs. A simple way to address this is to ask people what their needs are and create safe channels (including anonymous ones) for people to feed back. In practice, having open communication channels, regular meetings and staff available to respond to issues can go a long way to making the changes students and researchers from underrepresented groups need to know that their problems are being addressed.</p>
                    </div>
                    
                    <div class="row mt-4 ">
                        <h4 class="textgray mb-4">2. Set the tone</h4>
                        <p>Educational institutions welcome hundreds of new students every year, and even small research teams will welcome new members. Encouraging existing staff to begin introductions with pronouns helps to set an atmosphere in which it’s clear that people of all genders are included and accepted.</p>
                        <p>It’s important to acknowledge a paradox when placing inclusivity projects within an induction. While new students and researchers need to be introduced to the tone and culture of an organisation, it’s also vital that this isn’t a single tick-box exercise. New members are taking on so much information about the organisation they have joined, that inclusivity training during induction can contribute to cognitive overload and be dismissed.</p>
                        <p>Acknowledging this paradox can be really helpful in understanding why repeated inclusivity projects and events are needed.</p>
                    </div>
                    
                    <div class="row mt-4 ">
                        <h4 class="textgray mb-4">3. Celebrate success</h4>
                        <p>High profile inclusivity events throughout the year both celebrate the work students and researchers are doing to make an institution more inclusive and help to combat the feeling of inertia that researchers identified as a barrier to inclusive practice. Events can include awards for staff and departments who have changed practical elements of their work to make it more inclusive, individual grants to help members of the community design inclusive, ways of working and communicating the work of DEI teams.</p>
                        <p>It’s important to acknowledge a paradox when placing inclusivity projects within an induction. While new students and researchers need to be introduced to the tone and culture of an organisation, it’s also vital that this isn’t a single tick-box exercise. New members are taking on so much information about the organisation they have joined, that inclusivity training during induction can contribute to cognitive overload and be dismissed.</p>
    
                        <p>For inspiration on DEI projects to create more inclusive workplaces, read:</p>
    
