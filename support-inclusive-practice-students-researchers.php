                        <p>High profile events create the possibility for communicating the importance of inclusivity throughout the year, expanding the focus away from induction and training. They also contribute to a positive feeling around inclusive practice that recognises achievements. This in turn can make it easier to acknowledge problems, since they become something the institution can act upon and solve.</p>
    
                    </div>
                    
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">4. Be open to gaps and missteps</h4>
                        <p>In large organisations mistakes will happen, gaps in culture will appear and best practice will sometimes fall by the wayside. Diversity mentoring for staff who want to fill the gaps in their knowledge of inclusive practice will help them to set the tone for researchers and students around them.</p>
                        <p>Mentoring allows staff a space to ask questions they may be too embarrassed to ask, challenges potential misconceptions about the experience of others, and helps reduce defensiveness about subjects researchers and staff are trying to understand.</p>
                    </div>
                    
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">How to build a positive cycle for inclusive research</h4>
                        <p>Together, these four practical approaches can create a virtuous circle. Listening and setting the tone help to create clear expectations of inclusive practice. Celebrating success makes these efforts ongoing and high profile. Creating space for people to learn and admit their missteps helps them to be better listeners.</p>
                        <p>Supporting researchers and students to be aware of, and implement, inclusive practice is an ongoing challenge. By creating data from individual experience, projects like the survey behind Insights into Diversity, Equity and Inclusion in the global research community can help guide the research community towards practical action that can have a positive impact on those individual experiences of science and research.</p>
                        <p>The next blog in this series takes a look at some of the technical aspects of making published science more accessible to readers.</p>
