                <div class="row mt-4">
                    <p>Making digital resources accessible to disabled users who experience the internet in different ways has always had an ethical imperative, but this is now backed up by equalities legislation such as the Americans with Disabilities Act and the European Accessibility Act.</p>
                    <p>The complexities around providing accessible digital resources can seem daunting, but they go to the heart of what a library is, and what librarians do; preserve academic knowledge so that students and researchers can study it. Open access widens the pool of people who can study research by removing paywalls, and best practice on digital accessibility does the same by removing other barriers.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray mb-4">Who are disabled library patrons and what are their needs?</h4>
                    <p>Equalities legislation reflects the fact that people with a disability are a large, but often underestimated, section of the population. This may be particularly true in academic research, where love of the subject sometimes drives people to work long past retirement age. As we live and work longer, age related disabilities mean that accessing digital resources with some kind of additional need is a when not an if. This doesn’t just mean changes to sight and hearing, but also age-related conditions such as arthritis and loss of fine motor skills.</p>
                    <p>That’s not to say that older researchers are the only people who should be considered when exploring access. Each experience of disability is unique, and the needs of any given institution will change with every student cohort. Ensuring the right tools are in place so that everyone can access the resources they need means institutions must constantly listen, adapt, and adjust. Disabilities themselves can change, especially chronic and progressive conditions, or after medical intervention. A disabled person may need more than one assessment of their needs within the time of their undergraduate degree.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray mb-4">Remote access and disabled library users</h4>
                    <p>Since 2020 working from home and accessing digital resources off campus has become an everyday way of working for many more researchers, but this shift arguably has a more profound impact for physically disabled users who can then access published resources from home, in an environment they have set up for their work and study needs.</p>
                    <p>For users, it seems obvious that they should be able to access library resources anywhere. Librarians understand that this is more complex than it appears, but when it comes to articles there are ways of making access to the version of record easier for remote users.</p>
                    <p>If remote access and digital resources make things easier for disabled users, it is then self-evident that those resources must also be accessible, which isn’t always as simple as it first appears.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray mb-4">How to make the library more accessible</h4>
                    <p>Accessible digital resources aren’t only essential for the practice and study of science, they attract future scientists. A university website is usually the first experience a prospective student will have of a university – even before a campus visit. Whether the website is accessible will affect where they apply. Making library resources accessible is key to a student’s experience of university. The webinar on library accessibility shares best practices, and the slide decks are available to download for easy reference.</p>
                    <p>A first step to understanding web accessibility is appreciating that the experience of using the internet for disabled people online content can be profoundly different for disabled people. Blind users will use screen readers and apps to navigate online information. For insight into how these work the UK’s Royal National Institute for the Blind has a regular podcast discussing new apps.</p>
                    <p>Some users will interact with websites and scholarly content using an adjusted keyboard or a screen reader, but they may also use a foot mouse, keyguard, or eye tracking system, and it’s worth exploring these to gain insight into how they navigate through menus, scroll and allow users to interact with the digital environment.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray mb-4">Procurement and web accessibility tools</h4>
                    <p>Library services are complex, and usually made up of different services from publishers and digital and physical vendors. With procurement such a large element of the behind-the-scenes work of the library, accessibility has to be embedded into this process. A Voluntary Product Accessibility Template (VPAT) allows vendors to state the accessibility of their products, but librarians have to be sure they are equipped to assess these, and have a working knowledge of web content accessibility guidelines.</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray mb-4">An accessible library is a successful library</h4>
                    <p>Librarians are heavily invested in making research as accessible as possible for the widest possible audience. This means responding to the changing needs of the specific academic community in a myriad of different ways. When responding to the needs of disabled users it’s important that the technology and adjustments deployed are the ones that will provide real solutions for them. This comes from listening and being willing to change services to respond to a change in need. Ultimately, ensuring that disabled library users can access the resources they need is just one facet of providing a library service to all users.</p>
                    <p>The last blog in this series describes practical steps to turn open access policies into reality, focusing on the evolving workflows and the role of librarians in this transformative landscape.</p>
