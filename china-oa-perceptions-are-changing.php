                            </div>
                        </div>
                        <p class="mt-4">In this blog, the first in a series on OA in China, we consider how perceptions have changed through a comparison of the 2024 survey (n = 1,593) and the responses from China in the APAC OA Attitudes Survey of 2021 (n = 657).</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Extensive awareness of OA</h4>
                        <p>The 2024 survey shows respondents to have extensive awareness of gold OA and its benefits. While 22% had a fully correct understanding of gold OA’s definition, agreeing with all 6 descriptions of gold OA, 68% had a basic understanding of gold OA and agreed with between one and five of the descriptions of gold OA. Only 10% of the respondents in China did not know what gold OA was.</p>
                    </div>
                    <div class="row mt-4">
                        <div class="col-lg-10">
                            <img src="assets/images/blog/108.webp" class="img-fluid img-100" alt=""/>
                        </div>
                        <p class="mt-4 mb-4"><i>Figure 1: Q. ‘Please select the options which you understand to correctly represent gold open access.’</i></p>
                        <p>This broad understanding of OA is also reflected in the fall in percentage of authors who did not choose gold OA because of a lack of awareness between 2021 and 2024. Whereas in 2021, 18% stated that they were not aware of gold OA as a publishing model, and 29% stated it did not occur to them, or they had never thought about gold OA, by 2024 only 8% said they were not aware of gold OA and 3% said it did not occur to them.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Improved perceptions and recognised benefits</h4>
                        <p>The perception of OA in China has also shown a discernible improvement between the survey of 2021 and that of 2024. When asked ‘how has your attitude towards open access changed in the past two years?’, the percentage of authors who felt less positive towards gold OA was 24% in the 2021 survey (n = 603) but had fallen to 15% in 2024 (n=1,379).  This improvement cannot be attributed to differences in sampling characteristics of the two surveys, but a real change in perceptions. </p>
                    </div>
                    
                    <div class="row mt-4">
                        <div class="col-lg-10">
                            <img src="assets/images/blog/107.webp" class="img-fluid img-100" alt=""/>
                        </div>
                        <p><i>Figure 2: Q. ‘How has your attitude towards open access changed in the past two years?’</i></p>
                        <p>When those who had become more positive about OA were asked to elaborate on their change in attitude, respondents typically focused on access to research for those who wouldn’t otherwise have access. OA was perceived to improve accessibility for people unaffiliated with universities and research institutions, as well as for international readers.</p>
                    </div>
                    
                    <div class="row">
                        
                        <div class="col-lg-10">
                            <div class="blue-banner">
                                <div class="row">
                                    <div class="col-lg-4 col-md-6 col-12 border-r1 p-0">
                                        <p>“I have been working with [medical] practitioners more often in the past few years. Subscription journals are inaccessible to them because they are usually not affiliated to institutions or universities. Open access journals help them know more about the latest research.”</p>
                                    </div>
                                    <div class="col-lg-4 col-md-6 col-12 border-r1 p-0">
                                        <p>“Open access makes the results of scientific research visible and available to all, regardless of financial means or institutional status.”</p>
                                    </div>
                                    <div class="col-lg-4 col-md-6 col-12 border-r1 p-0">
                                        <p>“The public accessibility is more important compared with the [processing] charge I have [to] pay out of my own pocket. I wish to hear feedback from international researchers concerning my publication, whether positive or negative.”</p>
                                    </div>
                                </div>
                            </div>
                        </div>
                        
                        <p class="mt-4">Accessibility and reach were also the areas of OA that resonated most strongly with authors in China when asked the extent to which they agreed with certain OA statements.  A significantly higher percentage of authors agreed or strongly agreed with positive perceptions of OA: OA benefits science with improved access, 75%; boosts citations and readership, 74%; helps advance their own subject area, 62%; is the future of publishing, 55%.  In each of these cases, the survey results of 2024 were at least 12% higher than in 2021.</p>
                    </div>
                    
                    <div class="row mb-4">
                        <div class="col-lg-10 mb-4">
                            <img src="assets/images/blog/106.webp" class="img-fluid img-100" alt=""/>
                        </div>
                        <p><i>Figure 3: Q. ‘To what extent do you agree or disagree with the following statements about OA publishing?’(% Agree & Strongly agree)</i></p>
                    </div>
                    <div class="row mt-4">
                        <h4 class='textgray mb-4'>Notable demographic differences</h4>
                        <p>Although the change in perceptions between 2021 and 2024 could not be attributed to differences in the sample demographic, there are some notable age and sector differences in OA attitudes. The younger generation is more open to the changing publishing landscape. For example, significantly more young researchers under 24 years old believe that open access benefits science and is the future of academic and scientific publishing.</p>
                    </div>
                    <div class="row mt-4">
                        <div class="col-lg-10">
                            <img src="assets/images/blog/105.webp" class="img-fluid img-100" alt=""/>
                        </div>
                        <p class="mt-4"><i>Figure 4: Q. ‘To what extent do you agree or disagree with the following statements about OA publishing?’ (% Agree & Strongly agree) by age group</i></p>
                        <p>From the sector perspective, compared to academic researchers, healthcare professionals are more likely (33% vs 21%) to advocate that all papers should go OA, and are less likely (24% vs 33%) to equate OA with lower quality. Obviously, the OA model has gained substantial momentum in medical research due to the critical need for widespread dissemination of health-related information. According to a comment from one survey respondent, not all medical institutions in China provide access to subscription-based journal content, especially less prestigious or well-funded hospitals. Medical practitioners might therefore have benefited more from reading OA publications and become more supportive of the OA publishing model.</p>
                    </div>
                    <div class="row mt-4">
                        <h4 class="textgray mb-4">Our approach to address OA concerns</h4>                        
                        <p>Despite the overall improvement in perceptions of OA, some respondents still have concerns. As one respondent commented “there are growing concerns of open access articles across academia regarding the integrity, credibility, and quality of research.” We understand those concerns and have been committed to rigour and excellence of the OA research we publish.</p>
                        <p>At Springer Nature, OA articles follow the same stringent standards as non-OA articles. We have a specialised and growing team working on research integrity throughout the research cycle. They prevent and resolve integrity problems and support our editor and reviewer network, sharing knowledge and insights and providing training on how to mitigate risks and detect misconduct. Besides our quality-control checks, we’re using the latest technologies, including AI, to identify unethical behaviour. We launched AI-enabled tools like Geppetto and SnappShot to trawl submissions across all our journals for indicators of suspicious text and manipulated images. Learn more about Research Integrity at Springer Nature.</p>
                        <p>In addition, Springer Nature publishes some of the world’s most influential OA brands and journals, including BMC, SpringerOpen, npj journals and Nature Communications. Authors submitting to Nature and the Nature research journals can also choose to publish their primary research using either the traditional publishing route or immediate gold OA.</p>
                        <p>Springer Nature has long been an advocate for the transition to OA, and the increase in awareness and perceptions of OA in China are welcome news for both the dissemination of knowledge and speed of future knowledge discovery. We will continue to explore new ways to support researchers and institutions on the path to OA, both in China and around the world.</p>
                        <p><i>Read our latest stories on OA transition: 2023 Open Access Report, and learn more about how Springer Nature’s approach to OA continues to benefit authors and institutions worldwide.</i></p>
