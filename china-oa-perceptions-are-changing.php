                     <div class="row">
                        <div class="col-lg-8 col-md-8 col-12">
                            <p class="textinfo">China is the world’s biggest producer of research articles. Chinese researchers’ attitudes to open access (OA) will be a key part of the global OA transition. How quickly the country moves to a full OA environment is also a vital step change in terms of moving forwards the wider global transition to open science. Therefore, Springer Nature conducted continuous surveys on China OA perceptions, to explore evolving attitudes and practice in the Chinese research community, further dig into key drivers and barriers behind their OA publishing and indicate how institutions can play a role to support their researchers to go OA.</p>
                        </div>
                        <div class="col-lg-4 col-md-4 col-12">
                            <div class="row align-items-center border-1 py-3">
                                <div class="text-box">
                                    <div>
                                        <img src="assets/images/blog/100.webp" class="img-fluid" alt=""/>
