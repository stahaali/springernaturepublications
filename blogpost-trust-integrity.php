                    <div class="row">
                        <p>Retractions have a bad reputation. If an article gets retracted, it seems someone somewhere must have done something terribly wrong or even committed misconduct. Indeed, the number of retractions in 2023 was at an all time high, fuelled by publishers retracting thousands of papers that were published as a result of compromised special issues.</p>
                        <p>Stories around these mass retractions, but also around those of individual cases, can be sensationalist: the allure of a tale of a PI gone rogue, fabricating data at his kitchen table or misappropriating research funds to buy yachts and sports cars is hard to resist. Where there is smoke, there is surely fire. </p>
                        <p>As a result, retractions have become conflated with misconduct. This is a problem. It makes authors hesitant to voluntarily retract their own articles. Science is supposed to be self-correcting, but authors fear the stigma that comes with having a retraction to their name. As part of a research integrity team at a major publisher, I’ve seen all the excuses: authors who would rather publish a correction the same length of the original article than to retract; authors who claim that the retraction is unfair because they didn’t commit misconduct… Or maybe one of the co-authors did, but surely they shouldn’t suffer the consequences. We see a lot of emotional pleas, which can be gut-wrenching: we are destroying their career and their livelihood by retracting their article. And last, but surely not least: authors lawyer up, threatening legal action to stop the retraction from happening.</p>
                        <p>There have been several proposals over the years along the lines arguing that we should have different types of retractions, one for honest errors, and another for actual misconduct. If authors can freely and without stigma retract for honest errors, science would be more self-correcting. Noted meta-researcher Daniele Fanelli suggested something similar back in 2016, and since then there have been multiple proposals to introduce different types of retractions, aimed at improving self-correction in science. Some authors suggested introducing three types, others, including Fanelli, went as far as coming up with five.</p>
                        <p>These are all well-intentioned proposals, but they are counterproductive. Distinguishing between honest retractions and retractions for actual misconduct will only increase the retraction stigma. It also places an undue burden on editors that would be impossible to fulfil. A retraction is an editorial decision. If we ask editors not only to focus on the reliability of the article, but also to pass judgment on authors’ conduct or misconduct, it would put them in a position where making the decision to retract would become harder. Editors rarely have sufficient information to confidently say that misconduct has taken place and to whom that misconduct could be attributed.</p>
                        <p>It will also put editors at risk: having to determine whether actual misconduct has taken place and then to publicly announce this in a retraction notice will likely lead to increased legal (and I would argue potentially physical) threats from authors against editors and publishers. I’m convinced that this would ultimately have a chilling effect, making editors more hesitant to retract. But more importantly, I’m convinced that we should not be asking this of editors at all.</p>
                        <p>Retractions are designed to be a neutral tool to correct the literature. Editorial decisions to retract articles should be based on the reliability of the research, and whether an editor still has confidence in the soundness of the science. Whether this loss of confidence is the result of suspected misconduct or an honest mistake, is ultimately irrelevant. Retractions should not be punitive, only corrective, and we should emphasize this.</p>
                        <p>It’s fair to say that to improve the reliability of the published record, more articles need to be retracted. In order to encourage all stakeholders (authors, editors and publishers) to do this, we need to foster an environment in which retractions are considered a corrective. Retractions should be a normal part of academic publishing and mean that editors and publishers are taking their responsibilities seriously. To do that we should stop ‘retraction shaming’: we need to emphasize to authors that a retraction of their article is not a form of punishment. Finally, we should encourage editors to consider that retractions are a force for good – a powerful and appropriate tool to correct the published record.</p>
                        <p>Retractions should be seen as what they are: corrections to the literature. It’s not fair on editors or honest authors to keep stigmatizing retractions. Don’t get me wrong: actual misconduct should be investigated and acted on, but it’s not up to a journal editor to punish an author. Institutions need to take their responsibility and investigate the alleged misconduct of their employees, while an editor’s responsibility lies with preserving the integrity of the published record.</p>
