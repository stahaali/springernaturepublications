                <div class="row mt-4">
                    <h4 class="textgray">1. Co-creating clean energy solutions </h4>
                    <p>Rosemary Idem emphasised that transition to clean energy must consider regional ways of life to avoid leaving anyone behind. For example, in South America and Latin America >50% of the population can access clean cooking fuel sources, compared with 10% in sub-Saharan Africa. There are also conflicting demands: people in the Global South want to access clean cooking sources, but some regions’ economies rely on fossil fuels. 
                        Runa Das argued the energy crisis means different things to different people, and that policy setters and decision makers must consider who they listen to. “Gender, race and other identity factors matter for both the questions we ask and the solutions we find,” she said.</p>
                    <p>“It is very important that we have localised policies,” added Abhishek Kar. “These are diverse regions with national decision-making. Any action towards energy justice needs to be politically and socially acceptable.”</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray">2. An enabling environment for clean energy transition</h4>
                    <p>Around 2.4 billion people globally use fuels like firewood, charcoal, coal and kerosene for their everyday cooking, heating and lighting needs. This has far-reaching impact on health, says Elisa Puzzolo, with the World Health Organization estimating 3.2 million people globally die prematurely each year due to household air pollution. </p>
                    <p>“A strong, national enabling environment for transition to clean household energy is needed,” says Puzzolo. “This should include fuel price policies, regulatory frameworks for appliances, fuel distribution and use, and enforcement – there is currently no compliance with risk management of these fuels and technologies.”</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray">3. Learning from others</h4>
                    <p>Past attempts to switch consumers to clean energy through reduced energy costs failed, as consumers soon returned to normal habits. Yet, Dr Wei-Jen Lee shared that when Texas energy companies provided night-time solar energy for free and charged a premium rate for day-time solar power, 80-95% residential usage shifted to the free time-zone, demonstrating that consumers will switch to cheaper, cleaner energy sources if the incentives are high enough.</p>
                    <p>Three quarters of Costa Rica’s carbon emissions comes from transport. Irene Cañas Díaz shared their approach to decarbonising the economy, first educating people on the health impact of air quality and introducing policies to stop import of old, inefficient cars. “Previously, people paid higher tax on newer, efficient cars. We reversed this so new electric cars could be imported tax-free. Now we have cleaner cars circulating and we’re introducing policies to improve public transport.”</p>
                </div>
                <div class="row mt-4">
                    <h4 class="textgray">4. Investing in clean energy-associated technologies </h4>
                    <p>As clean energy investment grows, investment in unforeseen areas is required, such as lithium supply for battery cells. These industries provide livelihoods and create opportunities for the most vulnerable countries in the world but as Francesca Larosa warned, “We must be mindful that climate action has impacts and repercussions on other sustainable development dimensions. We need to understand the synergies, trade-offs and risks of introducing new regional dividing lines when investing in clean energy transition.”</p>
                </div>
                <div class="row mt-4">
                    <p>Simon Evans contrasted the success of different narratives about climate change, arguing that although Al Gore’s film, An Inconvenient Truth, changed people’s minds, it did not facilitate adoption of climate policy. By contrast, Biden’s Inflation Reduction Act proved more successful as it focused on promising to deliver prosperity to American families.</p>
                    <p class="mb-4">Researchers trying to influence decision-makers must tailor their pitch to whoever can do most with it, considering policy cycles and decision points, said Hannah Stafford, while Bruno Takahashi urged communicators to think about their objective when deciding on tactics. “Knowledge is a necessary but insufficient factor in decision-making,” he said. “People’s perceptions, values, self-efficacy and social norms are probably more important.” </p>

