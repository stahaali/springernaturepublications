                        <h4 class="textgray2">What happens to transferred papers after an editor rejects them?</h4>
                        <p>After a paper is rejected from a journal, its authors are offered a chance to transfer (unless the editor marks them as unsuitable, for example because it is the wrong type of content). The paper is then referred to Transfer Desk. We use a tool called Freshdesk to track and process incoming papers, and every time a manuscript is offered a transfer, a record of the offer is created in this software. This record also contains all the details of the manuscript in question, and its submission history.</p>
                        <p>The paper is assigned to a subject expert with specialist knowledge on the research topic area, known as an Editorial Submission Advisor (ESA). Before the ESA begins suggesting journals, they first perform a light quality assessment to check the paper is up to publication standard. If the paper is not withdrawn on the basis of quality, they analyze the manuscript and choose the most suitable journals from our portfolio, with the help of a journal-matching algorithm. They use a mixture of the following criteria in order to find the most ideally-suited journals:</p>
                        
                        <div class="list-1 list-3">
                            <ul>
                                <li> <i class="fa-solid fa-check"></i> Comparisons between prospective journal scope and manuscript research topic </li>
                                <li> <i class="fa-solid fa-check"></i> Editorial characteristics of donating journal </li>
                                <li> <i class="fa-solid fa-check"></i> Donating journal business model </li>
                                <li> <i class="fa-solid fa-check"></i> Recommendations from the donating journal editor </li>
                                <li> <i class="fa-solid fa-check"></i> Notes on submission preferences from the potential journals </li>
                                <li> <i class="fa-solid fa-check"></i> Author preferences  </li>
                            </ul>
