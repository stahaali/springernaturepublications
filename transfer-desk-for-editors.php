                        <h4 class="textgray2 mb-4">How Does Transfer Desk Implement Quality Control?</h4>
                        <p>Transfer Desk applies several levels of quality control to all incoming papers.</p>
                        <p>Firstly, every paper undergoes an assessment process to eliminate truly unpublishable content. This can mean, among other things, plagiarized submissions or research rooted in prejudicial or ideological premises.</p>
                        <p>Authors whose research is competently executed and presented (but perhaps not impactful or novel) are encouraged to submit to our sound science portfolio.</p>
                        <p>We aim to recommend journals that are slightly less selective than the donor in order to increase the authors’ chance of publication.</p>
                        <p>Transfer Desk’s quality control mechanisms are robust, but we appreciate donor journals that are willing to opt papers out of transfers if they are unsuitable for publication in any journal.</p>

                        <h4 class="textgray2 mt-4 mb-4">Can editors control their transfer inflow by preference?</h4>
                        
                        <p>Yes, they can.</p>
                        <p>For editors who feel they are receiving more transfers than is ideal, Transfer Desk can take steps to create smaller, more bespoke inflows. We can set parameters limiting the types of journal that can donate papers, or restrict the research topics a journal receives. This raises the likelihood of review for an incoming transfer, while reducing the number of transfers overall.</p>
                        <p>Conversely, Transfer Desk can also increase transfers to a journal. This can be done by making the journal more appealing to authors by highlighting its selling points, or by making efforts to promote this journal more often to authors. Transfer Desk can also build relationships between larger donors and target receivers to encourage authors to submit their papers in those directions.</p>

                        <h4 class="textgray2 mt-4">How do authors feel about transfers?</h4>
                        
                        <p>Authors respond very positively to transfers. Transfer Desk regularly surveys author opinions on the service, which show extremely high rates of satisfaction, with more than half of these clients willing to use the service again.</p>
                        <p>Authors rate the service highly even when they don’t actually take up the offer of a transfer, or indeed when they do take up the offer but don’t get successfully placed at a journal.</p>
                        <p>Transfers function as a dimension of support for authors within their scientific communities, even as their paper is being rejected. Rather than feeling as though they’ve made no progress on publishing their research, this caveat to rejection allows editors to offer encouragement to the author that their contributions might be valued elsewhere. At best, transfers speed authors towards the most suitable possible journals, and cut down on the amount of time they spend exploring unsuitable options.</p>

                        <h4 class="textgray2 mt-4">Are transfers likely to change in future?</h4>
                        
                        <p>There are a lot of plans for the future of transfers.These include the eventual introduction of a guided submission service that will help authors find the best journal for papers on their first submission, reducing the need for post-rejection transfers.</p>
                        <p>Additionally, it may become possible to offer to transfer reviews alongside manuscripts, and to broker arrangements with certain journals to guarantee review for pre-selected authors.</p>
                        <p>As technology improves, there’s scope for a lot of innovations that will make transfers more versatile, efficient, and effective.</p>

