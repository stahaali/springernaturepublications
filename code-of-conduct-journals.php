                                    <div class="list-1 list-3">
                                        <ul>
                                           <li><i class="fa-solid fa-check"></i> Editor(s) are expected to comply with the Journal’s peer review policy (e.g. open, single-blind, double-blind).</li>
                                           <li><i class="fa-solid fa-check"></i> Peer review is an essential component of the research publication. It aims to assess the validity of the reported research and suitability for journals’ scope and aims. In order to maintain the integrity of the published record the Editor(s) are expected to ensure that all manuscripts reporting primary research, or secondary analysis of primary research, accepted for publication in the Journal are peer reviewed by reviewers who are competent in a relevant field and/or have expertise in a relevant methodology, as judged by their publication record. Editor(s) should ensure absence of conflicts of interest (e.g. recent collaboration with authors) for all peer reviewers; special attention should be given to peer reviewers suggested by the authors of the manuscript.</li>
                                           <li><i class="fa-solid fa-check"></i> Editor(s) are expected to obtain a minimum of two peer reviewers for manuscripts reporting primary research or secondary analysis of primary research. It is recognized that in some exceptional circumstances, particularly in niche and emerging fields, it may not be possible to obtain two independent peer reviewers. In such cases, Editor(s) may wish to make a decision to publish based on one peer review report. When making a decision based on one report, Editor(s) are expected to only do so if the peer review report meets the standards set out below.
                                           <li><i class="fa-solid fa-check"></i> Peer review reports should be in English and provide constructive critical evaluations of the authors’ work, particularly in relation to the appropriateness of methods used, whether the results are accurate, and whether the conclusions are supported by the results. Editorial decisions should be based on peer reviewer comments that meet these criteria rather than on recommendations made by short, superficial peer reviewer reports which do not provide a rationale for the recommendations.</li>
                                           <li><i class="fa-solid fa-check"></i> Editor(s) are expected to independently verify the contact details of reviewers suggested by authors or other third parties. Institutional email addresses should be used to invite peer reviewers wherever possible. Each manuscript should be reviewed by at least one reviewer who was not suggested by the author.</li>
                                           <li><i class="fa-solid fa-check"></i> Manuscripts that do not report primary research or secondary analysis of primary research, such as Editorials, Book Reviews, Commentaries or Opinion articles, may be accepted without two peer review reports. Such manuscripts should be assessed by the Editor(s) if the topic is in the area of expertise of the Editor(s); if the topic is not in area of expertise of the Editor(s), such manuscripts should be assessed by at least one independent expert reviewer or Editorial Board Member.</li>
