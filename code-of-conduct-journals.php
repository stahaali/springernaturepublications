                                    <p class="mb-4">Concerning any data from human subjects, Editor(s) are expected to ensure that manuscripts accepted for publication comply with the Journal’s editorial policies and specific research requirements and ethics standards for their field.</p>
                                    <div class="list-1 list-3">
                                        <ul>
                                            <li><i class="fa-solid fa-check"></i> Editor(s) should be satisfied that all research involving human subjects, human material or human data that is accepted for publication in the Journal is ethical and has been performed in accordance with the World Medical Association Declaration of Helsinki (https://www.wma.net/policies-post/wma-declaration-of-helsinki-ethical-principles-for-medical-research-involving-human-subjects/) or a comparable national standard that aims to protect the safety and rights of human research participants. Such research must comply with the authors’ national and institutional ethics committee requirements for ethics approval and informed consent.</li>
                                            <li><i class="fa-solid fa-check"></i> Where some or all of the research has taken place in a country that is different from that of the authors’, approval should be obtained from an ethics committee that is local to where the research took place.</li>
                                            <li><i class="fa-solid fa-check"></i> Regulations regarding what type of study requires ethical approval vary world-wide. Where there are no local or national ethics committees or established regulations, Editor(s) should use the standards set out in the Declaration of Helsinki to decide whether there are any ethical concerns about the conduct of the research that would preclude publication. Guidance is available from COPE, in particular from the COPE document entitled Guidance for Editors: Research, Audit and Service Evaluations. Editor(s) should seek advice from the Publisher in difficult cases.</li>
                                            <li><i class="fa-solid fa-check"></i> In the case of use of human tissue, expressly including but not limited to stem cells, the research should comply with national and international regulations and guidelines on the use of such tissue for research. </li>
                                            <li><i class="fa-solid fa-check"></i> Regardless of any local ethics committee approval, research that appears to violate the Declaration of Helsinki or comparable national standards that aim to protect the safety and rights of human research participants, or any guidelines on the use of human tissue, should be investigated in accordance with COPE guidelines. Editor(s) should seek guidance from the Publisher in difficult cases.</li>
                                            <li><i class="fa-solid fa-check"></i> Manuscripts reporting research on human subjects, tissue or data should include a statement of independent ethics committee approval including the name of the ethics committee that approved the study and a statement of informed consent obtained from the human subjects to participate in the study.</li>
                                            <li><i class="fa-solid fa-check"></i> Where the manuscript reports information, data, images or videos that might potentially identify an individual, the manuscript should include a statement of informed consent to publish those details.</li>
