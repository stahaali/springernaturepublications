                <p>More general information on the role of a Journal Editor at Springer Nature can be found in our Editors’ Information Pack.</p>
                <p>The Code of Conduct for Editors sets out the standards and expectations associated with the role of Editor. These requirements are based on guidelines and best practice recommendations issued by organizations such as the Committee on Publication Ethics (COPE). Adhering to these will ensure that you are working to the highest standard of editorial practice. </p>
                <p>Springer Nature is a member of the Committee on Publication Ethics (COPE). COPE is an advisory body which provides guidance to editors and publishers on all aspects of publication ethics.</p>

