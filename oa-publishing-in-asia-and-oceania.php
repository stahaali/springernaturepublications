                        <h4 class="bigtext mb-4">Transformative agreements: Enabling researchers to publish OA</h4>
                        <p>How can you support your researchers to publish OA and get the benefits of increased reach and impact for their work? When your institution is part of an OA agreement, you can easily and effectively promote and advance OA publishing.</p>
                        <p>Transformative agreements (TAs) combine the costs of publishing OA and the costs of accessing subscription content in hybrid journals. They remove the burden of article processing charges (APCs) from researchers in participating institutions for publishing OA, and also grant them access to subscription content. With a TA in place, your library can create the optimal conditions for your researchers to do their best work and get the most impact for it.</p>
                        <p>At the end of 2023, Springer Nature’s TAs support researchers from over 3,500 institutions globally in publishing OA, and have a significant impact on the transition to a global and equitable OA landscape. Authors in participating institutions can easily choose gold OA and enjoy its benefits, regardless of their discipline. In humanities and social sciences (HSS), which are traditionally less well-funded, publishing OA can be challenging, but TAs are changing this. Over 90% of HSS OA content in Springer Nature’s hybrid journals is now published via a TA!</p>
                    </div>
