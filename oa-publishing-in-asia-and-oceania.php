                        <h4 class="textgray2 mb-4">Considerable impact of TAs on OA publishing in Asia Pacific</h4>
                        <p>The uptake of OA publishing in Asian and Oceanic countries that are part of a Springer TA has been considerable. These TAs have enabled researchers from all disciplines to publish OA: From HSS, from mathematics, physical, and applied sciences, and from medicine and life sciences. The first TA in Australia, for examples, began in 2022, and the total share of gold OA articles in hybrid journals soared from 8% in 2021 to 68% in 2022 and a whopping 75% in 2023.</p>
